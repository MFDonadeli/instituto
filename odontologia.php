                        <h3>Odontologia do Trabalho</h3>
                        <p>A odontologia do trabalho é uma especialidade que visa previnir e diagnosticar doenças do complexo bucomaxilofacial, provocadas pela atividade laboral, e a evitar acidentes do trabalho decorrentes de causas odontológicas, contribuindo assim para a saúde integral do trabalhador. Pode ser conceuituada como sendo "os conhecimentos de odontologia" aplicados a atenção em saúde do trabalhador nos aspectos de promoção, preservação, recuperação e a reabilitação da saúde de trabalhadores submetidos aos riscos e agravos advindos das condições de trabalho.</p>
