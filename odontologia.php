<li>assessoramento técnico e atenção em matéria de saúde, de segurança, de ergonomia e de higiene no trabalho assim como em matéria de equipamentos de proteção individual, entendendo-se inserido na equipe interdisciplinar de saúde do trabalho operante</li>
<li>planejamento e implantação de campanhas e programas de duração permanente para educação dos trabalhadores quanto a acidentes de trabalho, doenças ocupacionais e educação em saúde</li>
<li>organizar estatísticas de morbidade e mortalidade com causa bucal e investigar suas possíveis relações com as atividades laborais</li>
<li>realização de exames odontológicos para fins trabalhistas</li>
<li>identificar problemas bucais que podem colocar em risco a atividade laboral</li>
